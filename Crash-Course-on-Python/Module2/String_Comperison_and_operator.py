print("Nairobi" < "Milan" and "Nairobi" > "Hanoi")


# In this next example, "Nairobi" < "Milan" and "Nairobi" > "Hanoi", 
# the and logical operator is connecting two string comparison statements. 
# You learned previously that using the greater than and less than operators 
# on strings will test the alphabetical order (technically Unicode values) of the strings. 
# So, this complex comparison is checking if "Nairobi" is alphabetized before "Milan" (False) 
# AND after "Hanoi" (True). 